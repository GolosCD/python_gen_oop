from collections import defaultdict

class Grouper:
    def __init__(self,iterable,key):
        self.key = key
        self.groups = dict()
        [self.groups.setdefault(self.key(elem),[]).append(elem) for elem in iterable]
        
        
    def add(self,obj):
        self.groups.setdefault(self.key(obj),[]).append(obj)
        
    def group_for(self,obj):
        return self.key(obj)
        
    def __len__(self):
        return len(self.groups.keys())
        
    def __iter__(self):
        yield from self.groups.items()
        
    def __contains__(self,key):
        return key in self.groups
        
    def __getitem__(self,key):
        return self.groups.get(key)
        
        
