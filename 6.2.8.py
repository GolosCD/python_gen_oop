class HistoryDict:
    def __init__(self,data = dict()):
        for key in data:
            self.__dict__.setdefault(key,[]).append(data.get(key))
            
    def __setitem__(self,key,value):
        self.__dict__.setdefault(key,[]).append(value)
        
    def __getitem__(self,key):
        return self.__dict__.get(key)[-1]
    
    def __delitem__(self,key):
        del self.__dict__[key]
        
    def __len__(self):    
        return len(self.__dict__)
        
    def __iter__(self):
        yield from self.__dict__
        
    def keys(self):
       yield from self.__dict__
      
    def values(self):
       yield from (value[-1] for value in self.__dict__.values())
       
    def items(self):
       yield from ((item[0],item[1][-1]) for item in self.__dict__.items())   
     
    def history(self,key):
        return self.__dict__.get(key,[])
        
    def all_history(self):
        return self.__dict__    
        
        
